// Arithmatic Operation - +, -, *, /, % 

let a = 10;
let b = 4; 
console.log("a+b", a+b); 
console.log("a-b",a-b);
console.log("a*b",a*b);
console.log("a/b",a/b);
console.log("a%b",a%b);

// unary operator // increment 
console.log(a++); 
console.log(a);
//now a = 11
console.log(++a);
//decrement 
console.log(b--);
console.log(b);
//now b = 3 
console.log(--b);

// exponentiation 
let c = 5;
let d = 4;
console.log("c^d",c**d);


//Assignment Operators :- = , +=, -=, *=, %=, **=

let num = 5
 num += 4;
console.log(num); // num = num+5 -> 5+4 ->10
let num1 = 6;
 num1 -= 5;
console.log(num1); // num1 = num1 - 5 -> 6-5 ->1
let num2 = 7;
num2 *= 4;
console.log(num2);
let num3 = 8;
num3 %= 5;
console.log(num3);
let num4 = 10;
num4 **= 2;
console.log(num4);